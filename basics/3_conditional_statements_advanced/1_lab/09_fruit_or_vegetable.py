type_input = input()

if type_input == 'banana' or type_input == 'apple' or type_input == 'kiwi' \
        or type_input == 'cherry' or type_input == 'lemon' or type_input == 'grapes':
    print('fruit')
elif type_input == 'tomato' or type_input == 'cucumber' or type_input == 'pepper' \
        or type_input == 'carrot':
    print('vegetable')
else:
    print('unknown')
